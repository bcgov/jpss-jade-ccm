package ccm.models.system.justin;

public class JustinCrownAssignment {
  private String agency_name;
  private String agency_identifier;
  private String part_id;
  private String crown_staff_name;
  private String assign_type_code;
  private String assigned_date;
  private String end_date;


  public String getAgency_name() {
    return agency_name;
  }
  public void setAgency_name(String agency_name) {
    this.agency_name = agency_name;
  }
  public String getAgency_identifier() {
    return agency_identifier;
  }
  public void setAgency_identifier(String agency_identifier) {
    this.agency_identifier = agency_identifier;
  }
  public String getPart_id() {
    return part_id;
  }
  public void setPart_id(String part_id) {
    this.part_id = part_id;
  }
  public String getCrown_staff_name() {
    return crown_staff_name;
  }
  public void setCrown_staff_name(String crown_staff_name) {
    this.crown_staff_name = crown_staff_name;
  }
  public String getAssign_type_code() {
    return assign_type_code;
  }
  public void setAssign_type_code(String assign_type_code) {
    this.assign_type_code = assign_type_code;
  }
  public String getAssigned_date() {
    return assigned_date;
  }
  public void setAssigned_date(String assigned_date) {
    this.assigned_date = assigned_date;
  }
  public String getEnd_date() {
    return end_date;
  }
  public void setEnd_date(String end_date) {
    this.end_date = end_date;
  }



}
  